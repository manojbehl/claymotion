package com.claymotion.hasoffer.domain;

public class AdvertiserRawData {

	private String name;
	private String description;
	private String category;
	private int advertiser;
	private String androidPackage;
	private String revenueType;
	private double revenueRate;
	private int campaignId;
	private double storeRating;
	private String country;
	private String currency;
	private String appUrl;
	private int id;
	private int offerId;
	private String status;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public int getAdvertiser() {
		return advertiser;
	}
	public void setAdvertiser(int advertiser) {
		this.advertiser = advertiser;
	}
	public String getAndroidPackage() {
		return androidPackage;
	}
	public void setAndroidPackage(String androidPackage) {
		this.androidPackage = androidPackage;
	}
	public String getRevenueType() {
		return revenueType;
	}
	public void setRevenueType(String revenueType) {
		this.revenueType = revenueType;
	}
	public double getRevenueRate() {
		return revenueRate;
	}
	public void setRevenueRate(double revenueRate) {
		this.revenueRate = revenueRate;
	}
	public int getCampaignId() {
		return campaignId;
	}
	public void setCampaignId(int campaignId) {
		this.campaignId = campaignId;
	}
	public double getStoreRating() {
		return storeRating;
	}
	public void setStoreRating(double storeRating) {
		this.storeRating = storeRating;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getCurrency() {
		return currency;
	}
	public void setCurrency(String currency) {
		this.currency = currency;
	}
	public String getAppUrl() {
		return appUrl;
	}
	public void setAppUrl(String appUrl) {
		this.appUrl = appUrl;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getOfferId() {
		return offerId;
	}
	public void setOfferId(int offerId) {
		this.offerId = offerId;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	

}
